def addition(num1, num2)
  num1 + num2
end

def subtraction(num1, num2)
  results = num1 - num2
  puts results
end

def division(num1, num2)
  results = num1 / num2
  puts results
end

def multiplication(num1, num2)
  results = num1 * num2
  puts results
end

def modulo(num1, num2)
  results = num1 % num2
  puts results
end

def square_root(num)
  results = Math.sqrt(num)
  puts results
end
